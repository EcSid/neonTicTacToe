export function fromStringToNumber(value: string) {
	switch (value) {
		case 'first':
			return 1

		case 'second':
			return 2

		case 'third':
			return 3

		case 'four':
			return 4

		case 'five':
			return 5

		case 'six':
			return 6

		case 'seven':
			return 7

		case 'eight':
			return 8

		case 'nine':
			return 9
	}
}
